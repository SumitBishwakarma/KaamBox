const EmojiPicker = () => {
    const [searchQuery, setSearchQuery] = useState('');
    const [selectedCategory, setSelectedCategory] = useState('Smileys');
    const [recentEmojis, setRecentEmojis] = useState([]);
    const { toast } = useToast();

    const filteredEmojis = useMemo(() => {
        if (!searchQuery) return emojiData[selectedCategory] || [];
        const query = searchQuery.toLowerCase();
        return Object.values(emojiData).flat().filter(emoji => emoji.includes(query));
    }, [searchQuery, selectedCategory]);

    const copyEmoji = (emoji) => {
        navigator.clipboard.writeText(emoji);
        toast.success(`Copied ${emoji}`);
        setRecentEmojis(prev => {
            const updated = [emoji, ...prev.filter(e => e !== emoji)].slice(0, 20);
            return updated;
        });
    };

    const categories = Object.keys(emojiData);

    return (
        <div className="space-y-6">
            <div className="relative">
                <Search className="absolute left-3 top-1/2 -translate-y-1/2 text-[var(--text-muted)]" size={18} />
                <input
                    type="text"
                    value={searchQuery}
                    onChange={(e) => setSearchQuery(e.target.value)}
                    placeholder="Search emojis..."
                    className="input-field pl-10"
                />
            </div>

            {!searchQuery && (
                <div className="flex flex-wrap gap-2">
                    {categories.map(cat => (
                        <button
                            key={cat}
                            onClick={() => setSelectedCategory(cat)}
                            className={`px-3 py-1.5 rounded-lg text-sm transition-colors ${selectedCategory === cat
                                    ? 'bg-blue-500 text-white'
                                    : 'bg-[var(--bg-tertiary)] hover:bg-[var(--bg-secondary)]'
                                }`}
                        >
                            {cat}
                        </button>
                    ))}
                </div>
            )}

            {recentEmojis.length > 0 && !searchQuery && (
                <div>
                    <h3 className="text-sm font-medium text-[var(--text-secondary)] mb-2">Recently Used</h3>
                    <div className="flex flex-wrap gap-1">
                        {recentEmojis.map((emoji, i) => (
                            <button
                                key={i}
                                onClick={() => copyEmoji(emoji)}
                                className="text-2xl p-2 hover:bg-[var(--bg-tertiary)] rounded-lg transition-colors"
                            >
                                {emoji}
                            </button>
                        ))}
                    </div>
                </div>
            )}

            <div>
                <h3 className="text-sm font-medium text-[var(--text-secondary)] mb-2">
                    {searchQuery ? 'Search Results' : selectedCategory} ({filteredEmojis.length})
                </h3>
                <div className="grid grid-cols-8 sm:grid-cols-10 md:grid-cols-12 gap-1 max-h-[400px] overflow-y-auto p-2 bg-[var(--bg-tertiary)] rounded-xl">
                    {filteredEmojis.map((emoji, i) => (
                        <button
                            key={i}
                            onClick={() => copyEmoji(emoji)}
                            className="text-2xl p-2 hover:bg-[var(--bg-secondary)] rounded-lg transition-colors"
                            title="Click to copy"
                        >
                            {emoji}
                        </button>
                    ))}
                    {filteredEmojis.length === 0 && (
                        <p className="col-span-full text-center text-[var(--text-muted)] py-8">
                            No emojis found
                        </p>
                    )}
                </div>
            </div>

            <p className="text-sm text-[var(--text-muted)] text-center">
                Click any emoji to copy it to clipboard
            </p>
        </div>
    );
};

export default EmojiPicker;
